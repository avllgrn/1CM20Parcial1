#include <stdio.h>

int main(void){

    printf("%d\n",2+3);
    printf("%d\n",2-3);
    printf("%d\n",2*3);
    printf("%d\n",2/3);
    printf("%d\n\n",2%3);

    printf("2\t+\t3\t=\t%d\n",2+3);
    printf("2\t-\t3\t=\t%d\n",2-3);
    printf("2\t*\t3\t=\t%d\n",2*3);
    printf("2\t/\t3\t=\t%d\n",2/3);
    printf("2\t%%\t3\t=\t%d\n\n",2%3);

    printf("%d\t+\t%d\t=\t%d\n",2,3,2+3);
    printf("%d\t-\t%d\t=\t%d\n",2,3,2-3);
    printf("%d\t*\t%d\t=\t%d\n",2,3,2*3);
    printf("%d\t/\t%d\t=\t%d\n",2,3,2/3);
    printf("%d\t%%\t%d\t=\t%d\n\n",2,3,2%3);

    return 0;
}
